/**
 * reset_to_98 - Resets the value of an integer to 98.
 *
 * @n: Pointer to the integer to be reset.
 *
 * Description: This function takes a pointer to an integer and
 * updates the value it points to 98.
 *
 * Return: void
 */

void reset_to_98(int *n)
{
	*n = 98;
}
